void FoodMap::drawFood(sf::RenderWindow &window) {
    for(int j = 0; j < this->foodMap.size(); j++) {
        for (int i = 0; i < this->foodMap[j].size(); i++) {
            switch(foodMap[j][i]) {
                case 1: 
                    // std::cout << "Test" << std::endl;
                    food1.setPosition(sf::Vector2f(i * 8, j * 8));
                    window.draw(food1);
                    break;
                case 2: 
                    food2.setPosition(sf::Vector2f(i * 8, j * 8));
                    window.draw(food2);
                    break;
            }
        }
    }
}

bool FoodMap::eatFood(sf::Vector2f pacmanPos) {
    pacmanPos += sf::Vector2f(7.f,7.f);
    int posX = pacmanPos.x / 8;
    int posY = pacmanPos.y / 8;
    if(pacmanPos.x / 8.f == posX+0.5f && pacmanPos.y / 8.f == posY+0.5f && foodMap[posY][posX] != 0) {
        foodMap[posY][posX] = 0;
        return true;
    }
    return false;
}
